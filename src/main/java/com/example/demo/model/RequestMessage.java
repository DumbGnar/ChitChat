package com.example.demo.model;

public class RequestMessage {

    private int type;

    private String fromId;

    private String toId;

    private String content;

    public int getType() {
        return type;
    }

    public String getFromId() {
        return fromId;
    }

    public String getToId() {
        return toId;
    }

    public String getContent() {
        return content;
    }
}
